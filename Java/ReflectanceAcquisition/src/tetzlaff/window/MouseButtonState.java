package tetzlaff.window;

/**
 * Enumerates the possible states of a mouse button.
 * @author Michael Tetzlaff
 *
 */
public enum MouseButtonState
{
	/**
	 * The state of the button is unknown.
	 */
	Unknown, 
	
	/**
	 * The button is being pressed down.
	 */
	Pressed, 
	
	/**
	 * The button is not being pressed down.
	 */
	Released
}
